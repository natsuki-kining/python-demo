# 加
print("加(+)：2+2="+str((2+2)))

# 减
print("减(-)：2-2="+str((2-2)))

# 乘
print("乘(*)：2*2="+str((2*2)))

# 除
print("除(/)：2/2="+str((2/2)))

# 取整除
print("取整除(//)：2//2="+str((2//2)))

# 取余数
print("取余数(%)：2%2="+str((2%2)))

# 幂
print("幂(**)：2**2="+str((2**2)))

# 运算符的优先级,和数学中的运算符的优先级一致
print("运算符的优先级：(1+1)*2+2+(3+3)+2**3+3/3="+str((1+1)*2+2+(3+3)+2**3+3/3))